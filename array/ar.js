// Create an array
var ourarr = ["Pradeep" , 23];
var myarr = ["Prathap", 24];
console.log(ourarr);
console.log(myarr);

// Nested array
var ourarr = [["pradee" , 24],["prakas" , 23]];
console.log(ourarr);

// Access Array Data
var ourarr = [50 , 60 , 70];
var ourdata = ourarr[2];
console.log(ourdata);

// Modify Array Data
var ourarr = [50 , 60 , 70];
ourarr[2] = 30;
console.log(ourarr);

// Access Multi Dimensional Array with indexes
var myarr = [[2,3,5],[3,4,5],[5,7,8],[[1,3,5],6,8]];
var mydata = myarr[3][0][1];
console.log(mydata);
